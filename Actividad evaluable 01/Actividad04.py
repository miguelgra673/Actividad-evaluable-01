# Ejemplo de operador 'is'
a = [1, 2, 3]
b = a

print(a is b)  # True, ya que 'a' y 'b' apuntan al mismo objeto en memoria

# Ejemplo de operador 'not'
condicion = False

if not condicion:
    print("La condición es falsa")

# Ejemplo de operador 'in'
lista_numeros = [1, 2, 3, 4, 5]

if 3 in lista_numeros:
    print("El número 3 está en la lista")
